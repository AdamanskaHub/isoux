<h2 class="big-title mb-4">Card sorting (June 2022)</h2>

<p class="opacity-50 mb-8">Card sorting done on site with 5 people from different part of the company. Each card was a page on the website.</p>

<h3 class="title">What we've learned</h3>
<p>People didn't have time to finish and need more time</p>
<p>Some cards were removed as those pages seem to make no sense</p>
<p>One of the group divided cards between the public and members only pages</p>
<p>Many of the names of the pages didn't make sense to the people tested</p>
<p>People have a strong tendency to want to remember where a page was. However, it's so hard that they often can't</p>
<p>We will need to do more card sorting</p>
